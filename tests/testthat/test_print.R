context("sf: printing")

test_that("print integers", {
  m <- matrix(c(
    1054344, 1054347, 1054349, 1054351, 1054351, 1054355, 1054356, 1054365,
    1054362, 1054361, 4140707, 4140710, 4140704, 4140698, 4140686, 4140683,
    4140671, 4140667, 4140663, 4140659
  ), 10, 2)
  expect_equal(prnt.Matrix(m),
               "(1054344 4140707, 1054347 4140710, 1054349 4140704, 1054351 4140698, 1054351 4140686, 1054355 4140683, 1054356 4140671, 1054365 4140667, 1054362 4140663, 1054361 4140659)")
})

test_that("print variable sizes", {
  m <- matrix(c(
    1054344, 1054347.1, 1054349.01, 1054351, 1054351, 1054355, 1054356, 1054365,
    1054362, 1054361, 7, 4140710.0001, 414, 4140698, 4140686, 4140683,
    4140671, 4140667, 4140663, 4140659.000001
  ), 10, 2)
  expect_equal(prnt.Matrix(m), "(1054344 7, 1054347.1 4140710.0001, 1054349.01 414, 1054351 4140698, 1054351 4140686, 1054355 4140683, 1054356 4140671, 1054365 4140667, 1054362 4140663, 1054361 4140659.000001)")
})
